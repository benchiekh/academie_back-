-- Création de la table parents dans Supabase
-- Exécutez ce script dans l'éditeur SQL de votre projet Supabase

CREATE TABLE IF NOT EXISTS parents (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(255) UNIQUE NOT NULL,
  password VARCHAR(255) NOT NULL,
  role VARCHAR(50) DEFAULT 'PARENT' CHECK (role IN ('PARENT', 'ADMIN')),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Activer RLS (Row Level Security) pour la sécurité
ALTER TABLE parents ENABLE ROW LEVEL SECURITY;

-- Créer une politique pour permettre les opérations CRUD
CREATE POLICY "Parents CRUD Policy" ON parents
  FOR ALL USING (true) WITH CHECK (true);

-- Créer un index sur l'email pour des recherches plus rapides
CREATE INDEX IF NOT EXISTS idx_parents_email ON parents(email);

-- Créer un index sur la date de création pour le tri
CREATE INDEX IF NOT EXISTS idx_parents_created_at ON parents(created_at DESC);

